# Topic Name: Variables and Constants
# Learning Objective: Store values in names and understand values that should not change.
# Short Explanation: A variable is a named box for data. A constant is a value we agree not to change, written in uppercase by convention.
# Expected Output:
# Student: Alex
# School days in week: 5
# Common Mistakes:
# - Do not use unclear names like x1 when a meaningful name is possible.
# - Remember that Python is case-sensitive, so age and Age are different names.
# Interview Questions:
# - What is Variables and Constants?
# - Why is this topic useful in real programs?
# Best Practices:
# - Use clear names that explain the purpose of the value.
# - Keep examples small while learning, then combine concepts later.

# A variable can change while the program runs.
student_name = "Alex"

# A constant is written in UPPERCASE to show it should stay the same.
SCHOOL_DAYS_IN_WEEK = 5

# We print both values to prove the names point to useful data.
print("Student:", student_name)
print("School days in week:", SCHOOL_DAYS_IN_WEEK)
