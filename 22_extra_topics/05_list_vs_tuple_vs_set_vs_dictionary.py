my_list = ["red", "red", "blue"]
my_tuple = ("Monday", "Tuesday")
my_set = {"red", "blue"}
my_dictionary = {"name": "Alex", "age": 16}
print("List keeps order:", my_list)
print("Tuple is fixed:", my_tuple)
print("Set is unique:", my_set)
print("Dictionary uses keys:", my_dictionary)
